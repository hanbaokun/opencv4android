    Mat &i1 = *(Mat *) addr1;
    Mat &i2 = *(Mat *) addr2;

    Mat I1, I2;
    i1.convertTo(I1, d);           // cannot calculate on one byte large values
    i2.convertTo(I2, d);

    Mat I2_2 = I2.mul(I2);        // I2^2
